    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        try {
            String codigobarra = jTextField2.getText();
            String descripcion = jTextField3.getText();
            double costo = Double.parseDouble(jTextField1.getText());
            double margenganancia = Double.parseDouble(jTextField4.getText());
            String rubro = jComboBox1.getSelectedItem().toString();
            String marca = jComboBox2.getSelectedItem().toString();
            Double valor = Double.parseDouble(jComboBox3.getSelectedItem().toString());
            presentador.registrarProducto(codigobarra, descripcion, costo, margenganancia, rubro, marca, valor);
        } catch(Exception e) {
            mostrarMje("Error en uno o mas campos");
        }
    }//GEN-LAST:event_jButton2MouseClicked

