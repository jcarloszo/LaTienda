    @Override
    public void cargarListas(List<Marca> marcas, List<modelo.Color> colores, List<Rubro> rubros, List<PorcentajeIVA> porcentajesIVA) {
        
        //Marcas
        jComboBox2.removeAllItems();
        for(Marca m : marcas) jComboBox2.addItem(m.getDescripcion());
        
        //Colores
        jComboBox4.removeAllItems();
        for(modelo.Color c : colores) jComboBox4.addItem(c.getDescripcion());
        
        //Rubro
        jComboBox1.removeAllItems();
        for(Rubro r : rubros) jComboBox1.addItem(r.getDescripcion());
        
        jComboBox3.removeAllItems();
        for(PorcentajeIVA porc : porcentajesIVA) jComboBox3.addItem(porc.getValor()+"");
    }

    @Override
    public void listarTalles(List<Talle> talles) {
        jComboBox5.removeAllItems();
        for(Talle t : talles) jComboBox5.addItem(t.getDescripcion());
    }

    private void agregarFila(String color, String talle) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{color, talle});
    }

    @Override
    public void mostrarMje(String mje) {
        JOptionPane.showMessageDialog(null, mje);
    }
    
    @Override
    public void limpiar() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jComboBox4.setSelectedIndex(0);
        jComboBox3.setSelectedIndex(0);
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Color");
        model.addColumn("Talle");
        jTable1.setModel(model);
    }
