    @Override
    public void limpiar() {
        jTextField1.setText("");
        jTextField2.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jComboBox3.removeAllItems();
        jComboBox4.removeAllItems();
        
        DefaultTableModel model1 = new DefaultTableModel();
        model1.addColumn("COD");
        model1.addColumn("DESC");
        model1.addColumn("Rubro");
        model1.addColumn("Marca");
        model1.addColumn("Precio");
        jTable1.setModel(model1);
        
        DefaultTableModel model2 = new DefaultTableModel();
        model2.addColumn("COD");
        model2.addColumn("DESC");
        model2.addColumn("Talle");
        model2.addColumn("Color");
        model2.addColumn("Cantidad");
        model2.addColumn("Precio");
        model2.addColumn("SubTotal");
        jTable3.setModel(model2);
        
        limpiarFormCliente();
        
        jLabel8.setText("$0.00");
        jLabel16.setText("$0.00");
        jTextField7.setText("");
    }

