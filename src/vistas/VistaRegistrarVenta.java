    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        // TODO add your handling code here:
        try {
            Double monto = Double.parseDouble(jTextField7.getText());
            Double total = Double.parseDouble(jLabel8.getText().replace("$", ""));
            if(monto > total){
                Double res = monto - total;
                jLabel16.setText("$"+(Math.round(res*100.0)/100.0));
            } else {
                mostrarMensaje("El monto no es suficiente");
            }
            
        } catch (Exception e){
            mostrarMensaje("Error en uno de los campos");
        }
    }//GEN-LAST:event_jButton4MouseClicked

